    private void jBEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEliminarActionPerformed
        int filaSeleccionada = tablaProveedores.getSelectedRow();
            String nombre = tablaProveedores.getValueAt(filaSeleccionada, 1).toString();
           
            int mjs = JOptionPane.showConfirmDialog(this, "¿Esta seguro que desea eliminar a "+ nombre+" ?");
            if(mjs == JOptionPane.YES_OPTION){
                proveedores.eliminarProveedor(nombre);
                
                 DefaultTableModel modeloTablaProveedores = new DefaultTableModel(proveedores.getProveedor(), columnasProveedores);
                 tablaProveedores.setModel(modeloTablaProveedores);
                 JOptionPane.showMessageDialog(this, "Cliente eliminado exitosamente");
            }
    }//GEN-LAST:event_jBEliminarActionPerformed


