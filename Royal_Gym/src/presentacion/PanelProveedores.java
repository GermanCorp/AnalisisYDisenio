    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
         if (jTextFieldnombrepro.getText().isEmpty()
                && jTextFieldcuentabancaria.getText().isEmpty()
                && jTextFieldtelefonopro.getText().isEmpty()
                && jTextFieldtipoproveedor.getText().isEmpty()
                && jTextFieldcorreo.getText().isEmpty()
                && jTextFieldDreccionPro.getText().isEmpty()
                && jTextFieldContacto.getText().isEmpty()
                && jTextFieldTelefonoContacto.getText().isEmpty()
                 
                 
                 ) {
            JOptionPane.showMessageDialog(this, "ingrese Toda la Información", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (jTextFieldnombrepro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar el NOMBRE del proveedor", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (jTextFieldcuentabancaria.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar el numero de CUENTA BANCARIA del proveedor", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (jTextFieldtelefonopro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar el numero de TELEFONO del proveedor", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (jTextFieldtipoproveedor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar el TIPO DE PROVEEDOR", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (jTextFieldcorreo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe Ingresar el CORREO del proveedor", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (jTextFieldDreccionPro.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar la DIRECCION del proveedor", "Error!", JOptionPane.ERROR_MESSAGE);
        } else if (jTextFieldContacto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el CONTACTO del proveedor", "Error!", JOptionPane.ERROR_MESSAGE);
         } else if (jTextFieldTelefonoContacto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe ingresar el TELEFONO DEL CONTACTO del proveedor", "Error!", JOptionPane.ERROR_MESSAGE);    
            
        } else {

            proveedores.insertarProveedores(
               jTextFieldnombrepro.getText(),   
               jTextFieldcuentabancaria.getText(),
               jTextFieldtelefonopro.getText(),
               jTextFieldtipoproveedor.getText(),
               jTextFieldcorreo.getText(),
               jTextFieldDreccionPro.getText(),
               jTextFieldContacto.getText(),
               jTextFieldTelefonoContacto.getText());

            jTextFieldnombrepro.setText("");
            jTextFieldcuentabancaria.setText("");
            jTextFieldtelefonopro.setText("");
            jTextFieldtipoproveedor.setText("");
            jTextFieldcorreo.setText("");
            jTextFieldDreccionPro.setText("");
            jTextFieldContacto.setText("");
            jTextFieldTelefonoContacto.setText("");

        JOptionPane.showMessageDialog(this, "Registro Exitoso", "Exitoso", JOptionPane.INFORMATION_MESSAGE);
        DefaultTableModel modeloTablaProveedores = new DefaultTableModel(proveedores.getProveedor(), columnasProveedores);
        tablaProveedores.setModel(modeloTablaProveedores);
        
         } 
    }//GEN-LAST:event_botonGuardarActionPerformed

